// Operator: An operator is a symbol that tells the program what operation to perform (like add, subtract, compare).

//  Operand : An operand is the value or variable on which the operator works.

// Example: 10 + 5

//+ → Operator

//10 and 5 → Operands

void main() { 
  int a = 15;
  int b = 4;

  print("Addition: ${a + b}");
  print("Subtraction: ${a - b}");
  print("Multiplication: ${a * b}");
  print("Division: ${a / b}");
  print("Modulus: ${a % b}");
}




